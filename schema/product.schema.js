const mongoose = require("../db_init");
const { String, Number } = mongoose.Schema.Types;
const productSchema = new mongoose.Schema(
  {
    productId: {
      type: Number,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    stock: {
      type: Number,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    imgUrl: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: {
      createdAt: "createdAt",
      updatedAt: "updatedAt",
    },
  }
);

const Product = mongoose.model("Product", productSchema);
module.exports = Product;
